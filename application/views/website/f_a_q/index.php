<span style="text-decoration: underline; font-family: arial,helvetica,sans-serif; font-size: 14pt;"><strong>SEPUTAR KANTIN SEKOLAH ELETRONIK</strong></span>
<br>
<br>
<br>
<ol>
	<li><span style="font-size: 13pt; font-family: arial,helvetica,sans-serif;">Apa itu Kantin Sekolah Elektronik ?</span>
<br>
<ul>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Kantin Sekolah Elektronik adalah sistem yang di bangun untuk memudahkan produsen menghasilkan PJAS (Panganan Jajan Anak Sekolah) yang bermutu. Sistem PERTAMA dan SATU-SATUNYA di Indonesia yang menjual PJAS secara online dengan layanan antar (delivery service) ke sekolah sekolah. Menggunakan bahan baku bebas dari bahan yang berbahaya bagi tubuh manusia dengan proses yang hegienis.</span></li>
</ul>
<br>
</li>
	<li style="text-align: justify;"><span style="font-size: 13pt; font-family: arial,helvetica,sans-serif;">Kenapa ada Kantin Sekolah elektronik ?</span>
<br>
<ul>
	<li>
<p style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Kegelisahan akan banyaknya makanan dan jajanan di kantin  siswa yang menggunakan bahan terlarang, bahan pengawet, bahan tambahan pangan berlebihan dan makanan yang memiliki nilai gizi buruk. Disisi Lain tidak ada sistem yang membantu semua pihak turut mensukseskan  kantin sehat di sekolah.</span></p>
<p style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Panganan Jajan Anak Sekolah yang sehat dapat mendorong terciptanya generasi muda yang andal dan berkualitas. "Sarapan atau jajanan sehat melatih disiplin anak dan otak menjadi encer, sehingga daya serap anak saat belajar juga tinggi.</span></p>
</li>
</ul>
</li>
<br>
	<li style="text-align: justify;"><span style="font-size: 13pt; font-family: arial,helvetica,sans-serif;">Apa alasan bergabung dengan Kantin Sekolah Elektronik ?</span>
<br>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Ada beberapa alasan bila anda bergabung dengan KSE</span>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">MENARIK - Info nutrisi disetiap menu sarapan.</span></li>	
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">TERJADWAL - Menu sarapan dapat diprogram sampai dengan satu bulan ke depan.</span></li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">TERJAMIN - Didukung oleh pakar dan professional.</span></li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">AMAN - Pembelian tanpa menggunakan uang tunai.</span></li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">MUDAH - Pembelian dengan <em>ponsel</em>, <em>unduh</em> melalui playstore (gratis).</span></li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">CEPAT - Dengan barcode, tidak perlu antri untuk mengambil pesanan.</span></li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">NYAMAN - Siswa dapat menikmati waktu sarapan lebih panjang.</span></li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">LANGSUNG - Bermanfaat untuk kesehatan peserta didik.</span></li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">PRAKTIS - Menu kantin sekolahmu, langusng di <em>unduh</em> diwebsite.</span></li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">MENARIK - Info nutrisi disetiap produk yang di konsumsi.</span></li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">SEHAT - Semua menu makanan disupervisi oleh ahli gizi setiap hari.</span></li>


	
</ul>
</li>
</ul>
</li>
<br>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Apa yang membedakan Kantin Sekolah Konvensional (kantin sekarang) di banding dengan Kantin Sekolah Elektronik ?</span>
<br>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Kantin Sekolah elektronik sangat berbeda dengan kantin konvensional saat ini,</span>
<ul>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">kantin konvensional menjual makanan &amp; minuman dengan cara manual ( produk ada di tempat) sehingga bila barang tidak laku, di jual lagi dan lagi, sehingga menurunkan kualitas makanan. sedangkan KSE menjual makanan setelah ada order masuk, jadi  makanan lebih bermutu,sehat, bersih dan aman karena di sesuaikan dengan jenis &amp; jumlah orderan (tidak ada sisa)</span></li>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Kantin konvensional tidak menampilkan info nutrisi, sedangkan di KSE ditampilkan kandungan nutrisi sehingga orang tua / siswa mengetahui manfaat dari makanan  / minuman yang di konsumsi.</span></li>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">KSE pemesanan menggunakan teknologi modern (gadget) sedangkan Kantin konvensional belum menggunakannya.</span></li>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">KSE dalam pembayaran menggunakan menggunakan non tunai (deposit) sehingga siswa tidak kwatir dan orang tua bisa memantau anaknya apa saja yang di konsumsi tiap hari bahkan sebulan.</span></li>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">KSE di dukung oleh tenaga ahli gizi yang kompeten.</span></li>
</ul>
</li>
</ul>
</li>
<br>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Apa keuntungan gabung dengan KSE ?</span>
<br>
<ul>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Bagi kantin sekolah konvensional yang sudah beroperasi / berjualan di sekolah saat ini bisa bergabung dengan Kantin Sekolah Elektronik secara gratis dan mendapat fasilitas berupa edukasi dan bimbingan / pelatihan dari Tim IT KSE dan ahli gizi.</span></li>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Kantin konvensional bisa memasukkan menu secara online dan bisa di akses oleh sekolah-sekolah lain, sehingga bisa memperluas pasar.</span></li>
</ul>
</li>
<br>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Bagaimana system KSE bekerja ?</span>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">order / pesanan dari siswa akan di teruskan ke server cloud, ke dapur dan ke oulet KSE, Kantin anda bisa mendistibusikan / mendapat order makanan / minuman dari sekolah lain yang terdekat dengan lokasi anda, sehingga bila selama ini hanya bisa menjual makanan di satu lokasi, bergabung dengan KSE bisa lebih dari satu lokasi order. hal ini tentunya akan menambah keuntungan.</span></li>
</ul>
</li>
<br>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Bila sudah bergabung apa yang perlu dipersiapkan ?</span>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">bergabung sebagai siswa / member KSE</span>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">hal perlu disiapkan adalah bagi siswa SMP dan SMA bila sudah tergabung dengan KSE dan sudah mempunyai kartu dan sudah deposit, siswa cukup mendowload aplikasi KSE secara gratis di playstore, bagi siswa SD pemesanan bisa di lakukan orang tua.</span></li>
</ul>
</li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Bergabung sebagai Pengelola kantin</span>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">mempersiapkan gadget dan mendowload aplikasi KSE di playstore secara gratis, selanjutnya sudah siap menerima orderan.</span></li>
</ul>
</li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Bergabung sebagai Pemasok</span>
<br>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">siap menerima order sebagai pemasok bahan baku makanan dan minuman untuk dapur-dapur seluruh Indonesia</span></li>
</ul>
</li>
</ul>
</li>
<br>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Berapa biaya pembuatan kartu Kantin Sekolah Elektronik ?</span>
<br>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">biaya untuk mencetak kartu gratis, masa berlaku kartu 5 tahun untuk mahaiswa, 3 tahun utk SMP dan SMA, 6 tahun untuk SD</span></li>
</ul>
</li>
<br>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Apakah dengan hadirnya KSE ini, Kantin yang sudah berjalan saat ini masih bisa terus jualan ?</span>
<br>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">sangat bisa, justru dengan hadirnya KSE ini dapat melengkapi yang sudah ada, bisa bersinergi , bisa memberikan edukasi sehingga secara bertahap kantin - kantin semua bisa memenuhi standart, karena MEA sebentar lagi bergulir pastinya akan lebih sulit bersaing bila kantin-kantin saat ini tidak berbenah.</span></li>
</ul>
</li>
<br>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Saya UMKM kuliner, apakah bisa gabung juga di KSE ?</span>
<br>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">sangat bisa bergabung, karena semuanya bisa bergabung termasuk PKL kuliner, warung, catering, usaha rumahan dll, caranya wajib mempunyai produk yang baik layak konsumsi, daftarkan diri anda, selanjutnya KSE akan memberikan pendamping utk ahli gizi yang kompeten.</span></li>
</ul>
</li>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Saya tertarik sebagai investor/mitra, apakah bisa bergabung ?</span>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Anda bisa bekerjasama dengan kami, misal pembelian hardware, pencetakan kartu dll, semua bisa dikerjasamakan, segera hubungi kami.</span></li>
</ul>
</li>
<br>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Apakah program KSE berlaku di seluruh Indonesia ?</span>
<br>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">iya, betul sekali, sistem KSE menyeluruh bisa di pakai di sekolah-sekolah seluruh Indonesia</span></li>
</ul>
</li>
<br>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Hal-hal apa yang perlu di persiapkan pihak sekolah, supaya KSE bisa segera di implementasikan di sekolah - sekolah ?</span>
<br>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">mensosialisasikan program KSE ke siswa, walimurid, kantin dan guru </span></li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">memberikan data siswa untuk di masukkan ke system KSE sesuai format yang sudah ada di website</span></li>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">memberikan data kantin/dapur saat ini untuk dimasukkan ke system KSE sesuai format yang sudah ada di website</span></li>
</ul>
</li>
<br>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Apakah orang tua siswa bisa mengkonsumsi PJAS yang ada di KSE ?</span>
<br>
<ul>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">bisa, siswa bisa melakukan pemesanan lebih dari satu dan bisa memilih beragam jenis PJAS yang disediakan di sistem KSE, sehingga bisa di bawa pulang untuk di konsumsi orang tua.</span></li>
</ul>
</li>
<br>
	<li><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Seberapa amankah menggunakan sistem ini ?</span>
<br>
<ul>
	<li style="text-align: justify;"><span style="font-family: arial,helvetica,sans-serif; font-size: 13pt;">Sistem KSE menggunakan https: (security) setingkat perbankan sehingga sudah teruji aman dan lancar.</span></li>
</ul>
</li>
</ol>



